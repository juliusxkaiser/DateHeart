const zeroDecimalCurrencies = new Set(["BIF", "CLP", "DJF", "GNF", "ISK", "JPY", "KMF", "KRW", "PYG", "RWF", "UGX", "VND", "VUV", "XAF", "XOF"]);

function regionFromLocale(locale: string) {
  const parts = locale.replace("_", "-").split("-");
  for (let index = 1; index < parts.length; index += 1) {
    const part = parts[index];
    if (/^[a-z]{2}$/i.test(part)) return part.toUpperCase();
  }
  return undefined;
}

function normalizeRegion(region: string | null | undefined) {
  if (!region) return undefined;
  const normalized = region.trim().toUpperCase();
  return /^[A-Z]{2}$/.test(normalized) ? normalized : undefined;
}

export function budgetMarketFor(
  language: LanguageCode,
  input: { locales?: readonly string[]; timeZone?: string | null; regionOverride?: string | null } = {},
): BudgetMarket {
  const fallback = defaultBudgetMarketByLanguage[language];
  const localeRegion = input.locales?.map(regionFromLocale).find(Boolean);
  const region = normalizeRegion(input.regionOverride) ?? regionByTimeZone[input.timeZone ?? ""] ?? localeRegion ?? fallback.region;
  const currency = (region && currencyByRegion[region]) || fallback.currency;
  const locale = region ? `${language}-${region}` : fallback.locale;
  return { currency, locale, region };
}

function formatCurrencyAmount(amount: number, market: BudgetMarket) {
  const maximumFractionDigits = Number.isInteger(amount) || zeroDecimalCurrencies.has(market.currency) ? 0 : 2;

  try {
    return new Intl.NumberFormat(market.locale, {
      currency: market.currency,
      currencyDisplay: "narrowSymbol",
      maximumFractionDigits,
      minimumFractionDigits: maximumFractionDigits,
      style: "currency",
    }).format(amount);
  } catch {
    return `${amount} ${market.currency}`;
  }
}

export function formatBudgetLabel(budget: string, language: LanguageCode, market = budgetMarketFor(language)) {
  const copy = budgetCopy[language];
  if (budget === "Free") return copy.free;
  if (budget === "Unlimited") return copy.unlimited;

  const tier = budgetTierByValue[budget as Budget];
  if (tier === undefined) return budget;

  const amount = budgetThresholdsByCurrency[market.currency]?.[tier];
  if (!amount) return copy.tiers[tier];
  const formattedAmount = formatCurrencyAmount(amount, market);
  return copy.upToPlacement === "after" ? `${formattedAmount} ${copy.upTo}` : `${copy.upTo} ${formattedAmount}`;
}

export function formatNoAdsPrice(market: BudgetMarket) {
  const amount = noAdsPriceByCurrency[market.currency] ?? 4.99;
  return formatCurrencyAmount(amount, market);
}

