import { access, readFile } from "node:fs/promises";
import { budgets, categories, dateIdeas, durations } from "../src/dateIdeas.ts";
import { budgetMarketFor, labelFor, translations, verifiedLanguageCodes } from "../src/i18n.ts";

const errors = [];

function assert(condition, message) {
  if (!condition) errors.push(message);
}

for (const category of categories) {
  const count = dateIdeas.filter((idea) => idea.category === category).length;
  assert(count >= 20, `Category ${category} has too few ideas: ${count}`);
}

for (const budget of budgets) {
  const count = dateIdeas.filter((idea) => idea.budget === budget).length;
  assert(count >= 20, `Budget ${budget} has too few ideas: ${count}`);
}

for (const duration of durations) {
  const count = dateIdeas.filter((idea) => idea.duration === duration).length;
  assert(count >= 20, `Duration ${duration} has too few ideas: ${count}`);
}

for (const language of verifiedLanguageCodes) {
  const market = budgetMarketFor(language);
  const copy = translations[language];
  assert(copy.filters && copy.category && copy.budget && copy.duration, `${language} is missing filter copy.`);

  for (const category of categories) {
    assert(labelFor("category", category, language), `${language} missing category label: ${category}`);
  }

  for (const budget of budgets) {
    assert(labelFor("budget", budget, language, market), `${language} missing budget label: ${budget}`);
  }

  for (const duration of durations) {
    assert(labelFor("duration", duration, language), `${language} missing duration label: ${duration}`);
  }
}

const envExample = await readFile(".env.example", "utf8");
assert(/VITE_APP_SHARE_URL=https:\/\/\S+/.test(envExample), ".env.example must contain an HTTPS VITE_APP_SHARE_URL.");

const mainSource = await readFile("src/main.ts", "utf8");
assert(mainSource.includes("VITE_APP_SHARE_URL"), "Share URL env hook is missing from the app.");
assert(mainSource.includes("function buildShareUrl"), "Share links must be generated by a dedicated helper.");
assert(mainSource.includes('url.searchParams.set("idea", idea.id)'), "Share URL must include the idea id.");
assert(mainSource.includes('url.searchParams.set("category", filters.category)'), "Share URL must include the category filter.");
assert(mainSource.includes('url.searchParams.set("budget", filters.budget)'), "Share URL must include the budget filter.");
assert(mainSource.includes('url.searchParams.set("duration", filters.duration)'), "Share URL must include the duration filter.");
assert(mainSource.includes("function handleSharedLink"), "Shared idea links must be handled on app load.");
assert(/navigator\.share\(\{[\s\S]*url:\s*shareUrl[\s\S]*\}\)/s.test(mainSource), "Native share payload must include url.");
assert(!mainSource.includes("<small>${count}</small>"), "Filter category choices must not show idea counts.");
assert(!mainSource.includes("filterPanelSummary.textContent = `${category} · ${count}`"), "Filter summary must not show idea counts.");
assert(mainSource.includes('class="icon-button filter-action"'), "Filter action must stay in the top-left toolbar.");
assert(!mainSource.includes('<button class="filter-action"'), "Filter action must not return as a large stage card.");
assert(mainSource.includes("Capacitor.getPlatform()"), "Native platform detection must stay wired.");
assert(mainSource.includes("const SHOW_PLUS_ENTRY = true"), "DateHeart Pro entry must stay visible for native store purchases.");
assert(mainSource.includes("elements.plusButton.hidden = !SHOW_PLUS_ENTRY"), "Pro purchase action visibility must stay controlled by SHOW_PLUS_ENTRY.");
assert(mainSource.includes("purchaseStoreProduct(STORE_PRODUCTS.noAds)"), "Native no-ads purchase must use the app store billing product.");
assert(mainSource.includes("buyPlus(STORE_PRODUCTS.proMonthly)"), "Native monthly Pro purchase must use the app store billing product.");
assert(mainSource.includes("buyPlus(STORE_PRODUCTS.proYearly)"), "Native yearly Pro purchase must use the app store billing product.");

const storePurchaseSource = await readFile("src/storePurchases.ts", "utf8");
assert(storePurchaseSource.includes("capacitor-plugin-cdv-purchase"), "Native purchases must use the Capacitor store purchase plugin.");
assert(storePurchaseSource.includes("dateheart_no_ads"), "Native no-ads product id must be configured.");
assert(storePurchaseSource.includes("dateheart_pro_monthly"), "Native monthly Pro product id must be configured.");
assert(storePurchaseSource.includes("dateheart_pro_yearly"), "Native yearly Pro product id must be configured.");

try {
  await access("capacitor.config.ts");
} catch {
  assert(false, "Capacitor config is missing.");
}

const capacitorConfig = await readFile("capacitor.config.ts", "utf8");
assert(capacitorConfig.includes('appId: "com.czarletsgo.dateheart"'), "Native app id must stay stable.");
assert(capacitorConfig.includes('appName: "DateHeart"'), "Native app name must stay stable.");

if (errors.length > 0) {
  console.error(errors.join("\n"));
  process.exit(1);
}

console.log(
  JSON.stringify(
    {
      categories: categories.length,
      budgets: budgets.length,
      durations: durations.length,
      ideas: dateIdeas.length,
      verifiedLanguages: verifiedLanguageCodes.length,
      ok: true,
    },
    null,
    2,
  ),
);
